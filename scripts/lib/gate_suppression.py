def _iso_jst_now() -> str:
    return datetime.now(JST).isoformat(timespec="seconds")


def _iso_jst_from_epoch(epoch: float) -> str:
    return datetime.fromtimestamp(epoch, tz=JST).isoformat(timespec="seconds")


def _parse_iso(value: str) -> "datetime | None":
    if not value:
        return None
    s = str(value).strip()
    if s.endswith("Z"):
        s = s[:-1] + "+00:00"
    try:
        t = datetime.fromisoformat(s)
        if t.tzinfo is None:
            t = t.replace(tzinfo=JST)
        return t
    except (ValueError, TypeError):
        return None


def _canonical_source_id(root: str, abs_path: str) -> str:
    try:
        rel = os.path.relpath(abs_path, root)
    except ValueError:
        rel = abs_path
    return rel.replace("\\", "/")


def classify_event_kind(rel_source_id: str) -> str:
    """Return the event_kind label for a relative source path."""
    normalized = rel_source_id.lstrip("./").replace("\\", "/")
    for pattern, kind in _EVENT_KIND_PATTERNS:
        if pattern.match(normalized):
            return kind
    return "unknown"


def _alert_state_path(root: str) -> str:
    return os.path.join(root, _ALERT_STATE_REL)


def load_event_ledger(root: str) -> dict:
    """Load full alert_state.yaml. Returns a dict with at least gates/notifications/dashboard_events keys."""
    state_path = _alert_state_path(root)
    data: dict = {}
    if os.path.exists(state_path):
        try:
            with open(state_path, encoding="utf-8") as f:
                loaded = yaml.safe_load(f)
            if isinstance(loaded, dict):
                data = loaded
        except (yaml.YAMLError, OSError):
            data = {}
    if not isinstance(data.get("gates"), dict):
        data["gates"] = data.get("gates") or {}
    if not isinstance(data.get("notifications"), dict):
        data["notifications"] = data.get("notifications") or {}
    if not isinstance(data.get("dashboard_events"), list):
        data["dashboard_events"] = []
    return data


def save_event_ledger(root: str, state: dict) -> bool:
    """Persist alert_state.yaml atomically. Returns True on success."""
    state_path = _alert_state_path(root)
    try:
        os.makedirs(os.path.dirname(state_path), exist_ok=True)
        fd, tmp_path = tempfile.mkstemp(
            prefix=".alert_state.", suffix=".yaml.tmp",
            dir=os.path.dirname(state_path),
        )
        try:
            with os.fdopen(fd, "w", encoding="utf-8") as f:
                yaml.safe_dump(state, f, allow_unicode=True, sort_keys=False)
            os.replace(tmp_path, state_path)
        except Exception:
            try:
                os.unlink(tmp_path)
            except OSError:
                pass
            raise
        return True
    except (OSError, yaml.YAMLError):
        return False


def _event_key(event: dict) -> tuple:
    return (str(event.get("event_kind") or ""), str(event.get("source_id") or ""))


def _scan_event_sources(root: str) -> list:
    """Scan all P6 event source files. Returns list of (source_id, mtime_epoch, event_kind)."""
    found: list = []
    seen: set = set()
    for pattern in _EVENT_SOURCE_GLOBS:
        for path in glob.glob(os.path.join(root, pattern)):
            try:
                mtime = os.path.getmtime(path)
            except OSError:
                continue
            source_id = _canonical_source_id(root, path)
            if source_id in seen:
                continue
            seen.add(source_id)
            kind = classify_event_kind(source_id)
            found.append((source_id, mtime, kind))
    return found


def _dashboard_md_mtime(root: str) -> "float | None":
    dash_path = os.path.join(root, _DASHBOARD_REL)
    if not os.path.exists(dash_path):
        return None
    try:
        return os.path.getmtime(dash_path)
    except OSError:
        return None


def _source_mtime_epoch(root: str, event: dict) -> "float | None":
    """Return the current epoch mtime of the event's source file, or None if missing."""
    source_id = event.get("source_id")
    if not source_id:
        return None
    path = os.path.join(root, str(source_id))
    if not os.path.exists(path):
        return None
    try:
        return os.path.getmtime(path)
    except OSError:
        return None


def update_event_ledger(root: str) -> dict:
    """Scan event sources and update the ledger; return updated state dict.

    Algorithm:
      1. Load existing state (gates/notifications/dashboard_events).
      2. Resolve previously-unresolved events whose source file is now <=
         dashboard.md mtime (compared in epoch seconds, not ISO strings).
      3. Scan event sources for current mtimes; record any source newer
         than dashboard.md that isn't already tracked as unresolved.
         Re-opens previously-resolved entries if the source changed again.
      4. Prune resolved events beyond the keep limit.
      5. Persist atomically.
    """
    state = load_event_ledger(root)
    ledger: list = list(state.get("dashboard_events") or [])
    dash_mtime = _dashboard_md_mtime(root)
    now_iso = _iso_jst_now()

    # Step 2: resolve existing unresolved events using current source mtimes
    if dash_mtime is not None:
        dash_iso = _iso_jst_from_epoch(dash_mtime)
        for event in ledger:
            if not isinstance(event, dict):
                continue
            if event.get("resolved_at"):
                continue
            src_epoch = _source_mtime_epoch(root, event)
            if src_epoch is None:
                continue
            if src_epoch <= dash_mtime:
                event["resolved_at"] = dash_iso

    by_key: dict = {}
    for event in ledger:
        if isinstance(event, dict):
            by_key[_event_key(event)] = event

    # Step 3: scan sources newer than dashboard.md → add or re-open
    scanned = _scan_event_sources(root)
    for source_id, mtime, kind in scanned:
        if dash_mtime is not None and mtime <= dash_mtime:
            continue  # already reflected
        key = (kind, source_id)
        existing = by_key.get(key)
        source_mtime_iso = _iso_jst_from_epoch(mtime)
        if existing is None:
            new_event = {
                "event_kind": kind,
                "source_id": source_id,
                "source_mtime": source_mtime_iso,
                "first_seen_at": now_iso,
                "resolved_at": None,
            }
            ledger.append(new_event)
            by_key[key] = new_event
        elif existing.get("resolved_at"):
            # Source moved again after a prior resolution → re-open
            existing["source_mtime"] = source_mtime_iso
            existing["first_seen_at"] = now_iso
            existing["resolved_at"] = None
        else:
            # Already unresolved → refresh source_mtime for visibility
            existing["source_mtime"] = source_mtime_iso

    # Step 4: prune
    resolved = [e for e in ledger if isinstance(e, dict) and e.get("resolved_at")]
    unresolved = [e for e in ledger if isinstance(e, dict) and not e.get("resolved_at")]
    resolved.sort(key=lambda e: str(e.get("resolved_at") or ""), reverse=True)
    state["dashboard_events"] = unresolved + resolved[:_LEDGER_RESOLVED_KEEP]

    save_event_ledger(root, state)
    return state


def unresolved_events(state: dict) -> list:
    """Return list of unresolved dashboard events from state dict."""
    events = state.get("dashboard_events") or []
    return [e for e in events if isinstance(e, dict) and not e.get("resolved_at")]


def has_unresolved_dashboard_events(root: str) -> bool:
    """Phase C: True iff the ledger has any unresolved dashboard event.

    Side effect: persists an updated ledger. Safe to call on every P6 check.
    """
    state = update_event_ledger(root)
    return len(unresolved_events(state)) > 0


def event_ledger_detection_rate(root: str, expected_source_ids: list) -> float:
    """PoC helper: fraction of expected source_ids covered by current unresolved ledger.

    For a fixture with N known dashboard-relevant changes, this should return
    >= 0.95 when the ledger correctly tracks them.
    """
    if not expected_source_ids:
        return 1.0
    state = update_event_ledger(root)
    tracked = {str(e.get("source_id") or "") for e in unresolved_events(state)}
    expected = {str(s).replace("\\", "/") for s in expected_source_ids}
    if not expected:
        return 1.0
    hit = len(expected & tracked)
    return hit / len(expected)



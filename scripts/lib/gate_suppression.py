def should_suppress_p5(root: str, gate_status: GateStatus) -> bool:
    """P5 (殿手作業滞留) suppression check.

    Suppress when a gate is open AND all unread shogun inbox messages
    are monitoring reminders (in_progress_monitor_alert / reality_check_alert),
    meaning no genuinely NEW action items exist.
    """
    if not gate_status.has_open_gate:
        return False

    inbox_path = os.path.join(root, "queue/inbox/shogun.yaml")
    if not os.path.exists(inbox_path):
        return False
    try:
        with open(inbox_path, encoding="utf-8") as f:
            data = yaml.safe_load(f) or {}
    except Exception:
        return False

    unread = [
        m for m in (data.get("messages") or [])
        if not m.get("read", True)
    ]
    if not unread:
        return False  # nothing unread — P5 should not fire anyway

    # If ANY unread message is a genuine action/decision request, don't suppress
    genuine_types = {"action_required", "decision_required", "cmd_complete"}
    for m in unread:
        if m.get("type") in genuine_types:
            return False  # New real item → keep P5

    # All unread are monitor reminders → suppress
    return True


def should_suppress_stall(root: str, gate_status: GateStatus) -> bool:
    """見回り-1 (dashboard stall) suppression check."""
    return stall_is_gate_driven(root, gate_status)


def should_suppress_uncommitted(root: str, gate_status: GateStatus) -> bool:
    """見回り-6 (git uncommitted) suppression check."""
    if not gate_status.has_open_gate:
        return False
    return all_uncommitted_are_gate_runtime(root)
